package com.example.entpe.model;

public class UtilisateurHasEtablissement {
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Attributes /////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    private int idUtilisateur;
    private int idEtablissement;
    private boolean estAdmin;

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors ///////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * Constructeur principal
     * @param idUtilisateur est l'id de l'utilisateur dans la BDD
     * @param idEtablissement est l'id de l'établissement dans la BDD
     * @param estAdmin est à vrai si l'utilisateur est admin/patron de l'établissement
     */
    public UtilisateurHasEtablissement(int idUtilisateur, int idEtablissement, boolean estAdmin) {
        setIdUtilisateur(idUtilisateur);
        setIdEtablissement(idEtablissement);
        setAdmin(estAdmin);
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Setters ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public void setIdUtilisateur(int idUtilisateur) { this.idUtilisateur = idUtilisateur; }

    public void setIdEtablissement(int idEtablissement) { this.idEtablissement = idEtablissement; }

    public void setAdmin(boolean estAdmin) { this.estAdmin = estAdmin; }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Getters ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public int getIdUtilisateur() { return idUtilisateur; }

    public int getIdEtablissement() { return idEtablissement; }

    public boolean estAdmin() { return estAdmin; }
}
